
def add(lhs, rhs):
    return lhs + rhs

def minus(lhs, rhs):
    return lhs - rhs

def mul(lhs, rhs):
    return lhs * rhs

def divide(lhs, rhs) -> float:
    return lhs / rhs

def eq(lhs, rhs) -> bool:
    return lhs == rhs

def smaller(lhs, rhs) -> bool:
    return lhs < rhs

def greater(lhs, rhs) -> bool:
    return lhs > rhs

def greater_or_eq(lhs, rhs) -> bool:
    return lhs <= rhs

def smaller_or_eq(lhs, rhs) -> bool:
    return lhs >= rhs
